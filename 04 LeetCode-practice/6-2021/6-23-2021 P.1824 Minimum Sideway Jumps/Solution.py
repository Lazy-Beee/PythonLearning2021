class Solution:
    def minSideJumps(self, obstacles: list[int]) -> int:
        # frog = [lane, step_count]
        frogs = [[2, 0]]
        for point in range(1, len(obstacles)):

            remove = []
            for i in range(len(frogs)):
                frog = frogs[i]
                # No obstacle ahead
                if not frog[0] == obstacles[point]:
                    frogs[i][1] += 1
                # Obstacle ahead
                else:
                    remove.append(i)
                    # Check left and right
                    route = [1, 2, 3]
                    route.remove(frog[0])
                    if obstacles[point - 1] != route[0]:
                        frogs.append([route[0], frog[1] + 2])
                    if obstacles[point - 1] != route[1]:
                        frogs.append([route[1], frog[1] + 2])

            for j in range(1, len(remove) + 1):
                frogs.pop(remove[-j])

            min_step = min(frogs[i][1] for i in range(len(frogs)))
            for frog in frogs.copy():
                if frog[1] >= min_step + 2:
                    frogs.remove(frog)

        min_step = min(frogs[i][1] for i in range(len(frogs)))
        return min_step - len(obstacles) + 1


